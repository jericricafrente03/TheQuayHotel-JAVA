public class AirMediaPlatform {
    //=====================================Variable=================================================
    //-------------------------------------Constant-------------------------------------------------
    public static final String TAG = AirMediaPlatform.class.getSimpleName();
    //-------------------------------------Instance-------------------------------------------------
    private names     names;
    private int       image;
    //----------------------------------------------------------------------------------------------
    //==============================================================================================


    //====================================Constructor===============================================
    //----------------------------------------------------------------------------------------------
    public AirMediaPlatform(names names, int image)
    {
        this.names=names;
        this.image=image;
    }
    //----------------------------------------------------------------------------------------------
    //==============================================================================================



    //======================================Getter==================================================
    //----------------------------------------------------------------------------------------------
    public int getImage() {
        return image;
    }
    //----------------------------------------------------------------------------------------------
    public names getName() {
        return names;
    }
    public static enum names{

        ANDROID("ANDROID"),
        IOS("IOS"),
        WINDOWS("WINDOWS");

        private String name;

        names(String name)
        {
            this.name=name;
        }
    }
    //----------------------------------------------------------------------------------------------
    //==============================================================================================
}
