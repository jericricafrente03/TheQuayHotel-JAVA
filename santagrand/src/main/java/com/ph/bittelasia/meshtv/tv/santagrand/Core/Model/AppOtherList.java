package com.ph.bittelasia.meshtv.tv.santagrand.Core.Model;

public enum AppOtherList {

    //======================================Variable================================================
    //--------------------------------------Constant------------------------------------------------
    NETFLIX(0,"Netflix","netflix"),
    FACEBOOK(1,"Facebook","facebook"),
    YOUTUBE(2,"Youtube","youtube"),
    TWITTER(3,"Twitter","twitter");
    //----------------------------------------------------------------------------------------------
    //--------------------------------------Instance------------------------------------------------
    private int    appID;
    private String name;
    private String method;
    //----------------------------------------------------------------------------------------------
    //==============================================================================================



    //=====================================Constructor==============================================
    //----------------------------------------------------------------------------------------------
    AppOtherList(int appID, String name, String method)
    {
        this.appID  = appID;
        this.name   = name;
        this.method = method;
    }
    //----------------------------------------------------------------------------------------------
    //==============================================================================================

    //=======================================Method=================================================
    //---------------------------------------Getter-------------------------------------------------
    public int getAppID()
    {
        return appID;
    }

    public String getName()
    {
        return name;
    }

    public String getMethod()
    {
        return method;
    }
    //----------------------------------------------------------------------------------------------
    //==============================================================================================

}
